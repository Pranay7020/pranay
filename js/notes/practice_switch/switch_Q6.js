let grade = "a";

switch(grade){
    case 'a':
        console.log("Excellent");
        break;
        case 'b':
        console.log("Good");
        break;
        case 'c':
        console.log("Average");
        break;
        case 'd':
        console.log("Poor");
        break;
        case 'f':
        console.log("Fail");
        break;
        default:
        console.log("not grade");
}