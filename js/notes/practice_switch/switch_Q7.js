let browser = "chrome";

switch(browser){
    case 'chrome':
        console.log("Google Chrome");
        break;
        case 'firefox':
        console.log("Mozilla Firefox");
        break;
        case 'safari':
        console.log("Apple Safari");
        break;
        case 'edge':
        console.log("Microsoft Edge");
        break;
        default:
        console.log("Unknown Browser");

}